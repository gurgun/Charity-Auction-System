/**
 * The Status enum represents the various states that an antique collection can be in.
 */
public enum Status {
    /**
     * The collection has been sold.
     */
    SOLD,

    /**
     * The collection has been approved for auction.
     */
    APPROVED,

    /**
     * The collection has been disapproved for auction.
     */
    DISAPPROVED,

    /**
     * The collection is pending approval.
     */
    PENDING
}
